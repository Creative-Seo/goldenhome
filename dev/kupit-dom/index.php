<script type="text/javascript">
ymaps.ready(init);

function init(){ 
	$('#map').css({'width':'auto','height':'500px', 'margin-bottom':'20px'});    
    var myMap;
    myMap = new ymaps.Map("map", {
        center: [56.185547,40.717593],
        zoom: 9,
        controls: []
    });
    myMap.controls.add("zoomControl", {
        position: {top: 15, left: 15}
    });
	myCollection = new ymaps.GeoObjectCollection(null, {preset: 'islands#redIcon'}),
	$('.thumbnail').each(function(i, elem) {
		var arr = new Array($(elem).find('a').attr('href'),$(elem).find('img').attr('src'),$(elem).find('.tck').text(),$(elem).find('.caption li').text())
		ymaps.geocode(arr[2], {results: 1}).then(function (res) {
			var html = '<div class="popup">';
				html +=	'<a href="'+arr[0]+'"><img src="'+arr[1]+'" alt="" width="100" /></a>';
				html +=	'<div class="popup-text">';
				html +=	'<span>Коттедж</span><br>';
				html +=	'<span>'+arr[2]+'</span><br>';
				html +=	'<span><b>'+arr[3]+'</span><br>';
				html +=	'<a href="'+arr[0]+'">Подробнее</a>';
				html +=	'</div></div>';

			myCollection.add(new ymaps.Placemark(res.geoObjects.get(0).geometry.getCoordinates(),
			{ balloonContent: html},
			{ iconLayout: 'default#image',
			  iconImageHref: '<?php echo HTTP_SERVER; ?>img/min_marker.png',
			  iconImageSize: [40, 51],
			  iconImageOffset: [-20, -47],
			  
			  balloonContentSize: [289, 151],
			  balloonImageHref: '<?php echo HTTP_SERVER; ?>img/min_popup.png',
			  balloonImageOffset: [-144, -147],
			  balloonImageSize: [289, 151],
			  balloonShadow: true }));
		});
	});
	myMap.geoObjects.add(myCollection);
//	myMap.setBounds(myCollection.getBounds());
}
</script>
